/**
 * @description Performs searches based on the malware_ttps provided
 * @param {*} text The content of the script to scan
 * @param {*} rules The array with the rules to match
 * @returns An array with the hited results
 */
function scan (text, rules)
{
    let matches = [];

    rules.forEach(rule => {

        let pattern = new RegExp(rule.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'gi'),
            result = text.match(pattern);

        if (result && result.length > 0)
        {
            matches.push(rule.toString().replace(/\/gi$/, ''));
        }
    });

    return matches;
}

/**
 * @description Restores the fetch and XMLHttp API implemtations
 */
function unhook_fetch_XML ()
{
    if (window.fetch !== original_fetch)
    {
        window.fetch = original_fetch;
        console.log("[NPM Protector] Fetch was altered, restoring...");
    }

    if (window.XMLHttpRequest !== original_xmlrequest)
    {
        window.XMLHttpRequest = original_xmlrequest;
        console.log("[NPM Protector] XMLHttpRequest was altered, restoring...");
    }
    
}

/**
 * @description Launches a full scan to the website
 */
function perform_full_scan ()
{
    // Mutex to stop if threads are not found
    if (no_threads_found)
    {
        // Disable becaouse of noise
        let html_matches = [];
        
        // Extract inline scripts and scan
        let inline_scripts = document.querySelectorAll('script'),
            script_matches = undefined;
        
        inline_scripts.forEach(script => {
        
            if (script.textContent)
            {
                script_matches = scan(script.textContent, malware_ttps);
            }
        
        });
        
        // At least 3 matches
        if (html_matches.length > 3 || script_matches.length > 30)
        {
            //.log the user

            // Enable the mutex
            no_threads_found = false;

            alert(`✋📢[NPM Protector] The current website you are browsing is infected by the NPM supplay chain campaing!, leave the site as soon as possible. Open the terminal to see the hits`);
            console.log(`[NPM Protector] Hits: ${JSON.stringify(script_matches)}`)
    
            unhook_fetch_XML();
        }
        else
        {
            console.log("[NPM Protector] No threads found on this website");
        }
    }
}

